namespace Tablazor.Enums;

/// <summary>
/// Specifies the type of button to render.
/// </summary>
public enum ButtonType
{
    /// <summary>
    /// A regular button. This is the default.
    /// </summary>
    Button,

    /// <summary>
    /// A submit button
    /// </summary>
    Submit,

    /// <summary>
    /// A reset button
    /// </summary>
    Reset,

    /// <summary>
    /// A link styled as a button
    /// </summary>
    Link
}